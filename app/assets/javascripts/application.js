$(document).on('turbolinks:load', function() {
    // the strain_source id is auto generated by rails. I found it by
    // looking at the HTML of the strain form in the browser.
    var show_source_options = function(source) {
	if(source === "Other Lab"){
	    // Hide O'Meara Lab options
	    $("#strain_method_group").addClass("d-none");
	    $("#strain_Mutant_source_DNA_group").addClass("d-none");
	    $("#strain_upstream_check_primers_group").addClass("d-none");
	    $("#strain_downstream_check_primers_group").addClass("d-none");

	    // show Other Lab options
    	    $("#strain_reference_group").removeClass("d-none");
	    $("#strain_sequence_validated_group").removeClass("d-none");	    
	} else if(source === "O\'Meara Lab"){
	    // show O'Meara lab options
	    $("#strain_method_group").removeClass("d-none");
	    $("#strain_Mutant_source_DNA_group").removeClass("d-none");
	    $("#strain_upstream_check_primers_group").removeClass("d-none");
	    $("#strain_downstream_check_primers_group").removeClass("d-none");

	    // hide Other Lab options
	    $("#strain_reference_group").addClass("d-none");
	    $("#strain_sequence_validated_group").addClass("d-none");	    
	}
    }

    // update strain options once on load
    show_source_options($("#strain_source").children("option:selected").val());

    // update whenever strain_source changes
    $("#strain_source").on("change", function(event){
	show_source_options($(this).val());
    });
});
